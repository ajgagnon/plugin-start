interface AppSidebarProps extends React.ComponentProps<typeof Sidebar> {
  onNavigate?: (page: { section: string; page: string }) => void;
  currentPage?: { section: string; page: string };
}

export function AppSidebar({
  onNavigate,
  currentPage,
  ...props
}: AppSidebarProps) {
  const handleNavigate = (section: string, page: string) => {
    if (onNavigate) {
      onNavigate({ section, page });
    }
  };

  return (
    <Sidebar {...props}>
      <SidebarHeader>
        <VersionSwitcher
          versions={data.versions}
          defaultVersion={data.versions[0]}
        />
        <SearchForm />
      </SidebarHeader>
      <SidebarContent>
        {/* We create a SidebarGroup for each parent. */}
        {data.navMain.map((item) => (
          <SidebarGroup key={item.title}>
            <SidebarGroupLabel>{item.title}</SidebarGroupLabel>
            <SidebarGroupContent>
              <SidebarMenu>
                {item.items.map((subItem) => {
                  const isActive =
                    currentPage?.section === item.title &&
                    currentPage?.page === subItem.title;

                  return (
                    <SidebarMenuItem key={subItem.title}>
                      <SidebarMenuButton
                        onClick={() =>
                          handleNavigate(item.title, subItem.title)
                        }
                        isActive={isActive}
                      >
                        {subItem.title}
                      </SidebarMenuButton>
                    </SidebarMenuItem>
                  );
                })}
              </SidebarMenu>
            </SidebarGroupContent>
          </SidebarGroup>
        ))}
      </SidebarContent>
      <SidebarRail />
    </Sidebar>
  );
}
